public class JavaInicial {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
    		//Crear e inicializar variables
		byte bnumero = 1;
		short snumero = 12; 
		int inumero = 50;
		long lnumero = 123456;
		float fnumero = (float) 2.2;
		double dnumero = 22.22;
		
		char ctexto = 'c';
		String stexto = "Cadena de texto";
		
		boolean verdadero = true;
		boolean falso = false;

		System.out.println("Tipo byte: " + bnumero);
		System.out.println("Tipo short: " + snumero);
		System.out.println("Tipo int: " + inumero);
		System.out.println("Tipo long: " + lnumero);
		System.out.println("Tipo float: " + fnumero);
		System.out.println("Tipo double: " + dnumero);
		System.out.println("Tipo char: " + ctexto);
		System.out.println("Tipo String: " + stexto);
		System.out.println("Tipo Boolean: " + verdadero);
		System.out.println("Tipo Boolean: " + falso);
	  
    }
    
}
